public class SwitchCase {

    public static void main(String[] args) {
        int day = 10;

        switch (day) {
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("tuesday");
                break;
            case 3:
                System.out.println("Wedshhh");
                break;
            case 4:
                System.out.printf("thusday");
                break;
            default:
                System.out.printf("Nam kho tinh nhu ma");
        }
    }
}
